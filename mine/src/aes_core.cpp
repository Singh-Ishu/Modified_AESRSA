    // Column 2
    tmp[8] = gf_mul(0x02, state[8]) ^ gf_mul(0x03, state[9]) ^ state[10] ^ state[11];
    tmp[9] = state[8] ^ gf_mul(0x02, state[9]) ^ gf_mul(0x03, state[10]) ^ state[11];
