// Helper: Galois Field Multiplication
inline uint8_t gf_mul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    bool hi_bit_set;
    
    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    b >>= 1;

    if ((b & 1) != 0) p ^= a;
    hi_bit_set = (a & 0x80) != 0;
    a <<= 1;
    if (hi_bit_set) a ^= 0x1b;
    
    return p;
}

